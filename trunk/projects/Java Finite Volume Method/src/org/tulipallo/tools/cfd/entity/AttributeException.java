public class AttributeException extends RuntimeException {
    private String name;

    public AttributeException(final Throwable cause,
                              final String name) {
        super(cause);
        this.name = name;
    }

    public AttributeException(final String message, final Throwable cause,
                              final String name) {
        super(message, cause);
        this.name = name;
    }

    public AttributeException(final String message, final String name) {
        super(message);
        this.name = name;
    }

    public AttributeException(final String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }
}
